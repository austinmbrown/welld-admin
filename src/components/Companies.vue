<template>
  <div>
    <h2>Companies</h2>
    <table v-if="companies.length > 0">
      <tr>
        <th>ID</th>
        <th>Company</th>
      </tr>
      <tr v-for="company in companies">
        <td>{{company.id}}</td>
        <td>{{company.name}}</td>
      </tr>
    </table>
  </div>
</template>


<script>
  import axios from 'axios';

  export default {
    data: function() {
      return {
        companies: []
      }
    },
    mounted: function () {
      axios.get('/admin/companies')
      .then(
        (response) => {
          this.companies = response.data.companies;
        },
        (error) => {
          console.log(error);
        }
      )
    }
  }
</script>

<style>
  table, tr {
    border: 1px solid #2c3e50;
    border-collapse: collapse;
    margin: auto;
    min-width: 40%;
  }
  tr:first-of-type {
    border-bottom: 2px solid #2c3e50;
  }
  td, th {
    border: 1px solid #2c3e50;
  }
</style>
